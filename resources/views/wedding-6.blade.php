		function startCountdown(targetDate) {
			var countDownDate = new Date(targetDate).getTime();

			var x = setInterval(function() {
				var now = new Date().getTime();
				var distance = countDownDate - now;
				var days = Math.floor(distance / (1000 * 60 * 60 * 24));
				var hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
				var minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
				var seconds = Math.floor((distance % (1000 * 60)) / 1000);

				document.querySelector('#clockdiv .days').textContent = days;
				document.querySelector('#clockdiv .hours').textContent = hours;
				document.querySelector('#clockdiv .minutes').textContent = minutes;
				document.querySelector('#clockdiv .seconds').textContent = seconds;

				if (distance < 0) {
					clearInterval(x);
					document.getElementById('clockdiv').innerHTML = '<div class="expired">Expired</div>';
				}
			}, 1000);
		}

		document.addEventListener('DOMContentLoaded', function() {
			var targetDate = "{{ $data->tglMulai }}"; // Assuming $data->tanggal is in a format recognized by Date constructor
			startCountdown(targetDate);
		});
	</script>
